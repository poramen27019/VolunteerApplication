    @SuppressWarnings("StatementWithEmptyBody")
    @Override
    public boolean onNavigationItemSelected(MenuItem item) {
        // Handle navigation view item clicks here.
        int id = item.getItemId();

        if (id == R.id.nav_home) {
            // Handle the camera action
        } else if (id == R.id.nav_add_work) {
            Intent i = new Intent(Navigation.this, YourWorks.class);
            startActivity(i);
        }else if (id == R.id.nav_list) {
            Intent i = new Intent(Navigation.this,JoinEvent.class);
            startActivity(i);
        } else if (id == R.id.nav_profile) {
            Intent i = new Intent(Navigation.this, UserMember.class);
            startActivity(i);

        } else if (id == R.id.nav_logout) {

        } else if (id == R.id.nav_send) {
            Intent i = new Intent(Navigation.this, ChatHome.class);
            startActivity(i);
        } else if (id == R.id.nav_interesting) {
        }

        DrawerLayout drawer = (DrawerLayout) findViewById(R.id.drawer_layout);
        drawer.closeDrawer(GravityCompat.START);
        return true;
    }


